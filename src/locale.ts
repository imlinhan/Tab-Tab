let currentLocale: LocalePref = 'system';
let resolvedMessages: Record<string, string> = {};

function resolveLocale(pref: LocalePref): string {
  if (pref !== 'system') return pref;
  const lang = navigator.language;
  if (lang.startsWith('zh')) return 'zh_CN';
  return 'en';
}

export function getMessage(key: string, substitutions: string[] = []): string {
  let msg = resolvedMessages[key] || key;
  substitutions.forEach((s, i) => {
    msg = msg.replace(new RegExp(`\\$${i + 1}`, 'g'), s);
  });
  return msg;
}

export function getLocalePref(): LocalePref {
  return currentLocale;
}

export async function setLocale(pref: LocalePref): Promise<void> {
  currentLocale = pref;
  const lang = resolveLocale(pref);
  resolvedMessages = MESSAGES[lang] ?? MESSAGES['en'];
  try {
    await chrome.storage.local.set({ localePref: pref });
  } catch { /* ignore */ }
}

export async function initLocale(): Promise<void> {
  try {
    const { localePref = 'system' } = await chrome.storage.local.get('localePref');
    currentLocale = localePref as LocalePref;
  } catch {
    currentLocale = 'system';
  }
  const lang = resolveLocale(currentLocale);
  resolvedMessages = MESSAGES[lang] ?? MESSAGES['en'];
}
